package com.zavordigital.la_huerta;

import android.content.Context;
import android.database.sqlite.SQLiteDatabase;
import android.database.sqlite.SQLiteOpenHelper;
import android.database.sqlite.SQLiteDatabase.CursorFactory;

public class SQLiteHelper extends SQLiteOpenHelper {
	
	
	 
    //Sentencia SQL para crear la tabla de Clientes
    String sqlCreate = "CREATE TABLE favoritos ("+
										 " id INTEGER,"+
										 " receta_text TEXT ,"+
										 " url_ TEXT ,"+
										 " receta_nombre TEXT ,"+
										 " tiempo TEXT,"+
										 " votos TEXT ,"+
										 " recetaid TEXT )";
    String sqlCreateI = "CREATE TABLE ingredientes ("+
			 " id INTEGER,"+
			 " texto TEXT )";
    
    
    
   
 
    public SQLiteHelper(Context contexto, String nombre,
                               CursorFactory factory, int version) {
        super(contexto, nombre, factory, version);
    }
 
    @Override
    public void onCreate(SQLiteDatabase db) {
        //Se ejecuta la sentencia SQL de creación de la tabla
        db.execSQL(sqlCreate);
        db.execSQL(sqlCreateI);
        
    }
 
    @Override
    public void onUpgrade(SQLiteDatabase db, int versionAnterior, 
                          int versionNueva) {
        //NOTA: Por simplicidad del ejemplo aquí utilizamos directamente 
        //      la opción de eliminar la tabla anterior y crearla de nuevo 
        //      vacía con el nuevo formato.
        //      Sin embargo lo normal será que haya que migrar datos de la 
        //      tabla antigua a la nueva, por lo que este método debería 
        //      ser más elaborado.
 
        //Se elimina la versión anterior de la tabla
        db.execSQL("DROP TABLE IF EXISTS cat_productos");
 
        //Se crea la nueva versión de la tabla
        db.execSQL(sqlCreate);
    }
}
